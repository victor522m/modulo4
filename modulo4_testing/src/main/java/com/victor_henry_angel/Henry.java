package com.victor_henry_angel;

public class Henry {
    public float sumar(float num1, float num2) {
        return num1 + num2;
    }

    public float restar(float num1, float num2) {
        return num1 - num2;
    }

    public float calcularPIEntreN(float num1) {
        return (float) (Math.PI / num1);
    }
}